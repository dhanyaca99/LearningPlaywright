//0 - sunday, 1 - monday, 2 - Tuesday
let day =10 ;
switch (day) {
    case 0: 
       console.log("sunday");
       break;
    case 1:
        console.log("monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("wednesday");
        break;
    default:
        console.log("Invalid day");
}